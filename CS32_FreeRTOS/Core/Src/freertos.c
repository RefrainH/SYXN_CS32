void StartTaskAdc(void const * argument)
{
  #if 0
  WdgMonitorInit();
  #endif

  BreakerProtectorInit();
  BreakerAdcInit();
	
  for(;;)
  {
    adc_conversion_start(ADC1);
    BreakerAdcProc();
    IwdgFeed();
    #if 1
    PrintSysInfo();
    #endif
  }
}




